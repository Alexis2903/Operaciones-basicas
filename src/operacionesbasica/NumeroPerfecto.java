    private boolean esNumeroPerfecto(int numero) {
    int sumaDivisores = 0;

    for (int i = 1; i <= numero / 2; i++) {
     
        if (numero % i == 0) {
            sumaDivisores += i;
        }
    }
    return sumaDivisores == numero;
    }
   
